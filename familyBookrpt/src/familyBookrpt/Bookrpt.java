package familyBookrpt;

public class Bookrpt {

	private int reportNo;
	private String famId;
	private int bookNo;
	private String reportTitle;
	private String reportContents;
	private String readPeriod;
	private String registDate;
	
	public int getReportNo() {
		return reportNo;
	}
	public void setReportNo(int reportNo) {
		this.reportNo = reportNo;
	}
	public String getFamId() {
		return famId;
	}
	public void setFamId(String famId) {
		this.famId = famId;
	}
	public int getBookNo() {
		return bookNo;
	}
	public void setBookNo(int bookNo) {
		this.bookNo = bookNo;
	}
	public String getReportTitle() {
		return reportTitle;
	}
	public void setReportTitle(String reportTitle) {
		this.reportTitle = reportTitle;
	}
	public String getReportContents() {
		return reportContents;
	}
	public void setReportContents(String reportContents) {
		this.reportContents = reportContents;
	}
	public String getReadPeriod() {
		return readPeriod;
	}
	public void setReadPeriod(String readPeriod) {
		this.readPeriod = readPeriod;
	}
	public String getRegistDate() {
		return registDate;
	}
	public void setRegistDate(String registDate) {
		this.registDate = registDate;
	}
}
